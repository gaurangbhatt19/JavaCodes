public class BitwiseOperators {
    public static void main(String[] args){
        int a=2;
        int b=1;
        System.out.println(a<<1);
        System.out.println(a>>1);
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(~a);
        System.out.println(a^b);
    }
}
